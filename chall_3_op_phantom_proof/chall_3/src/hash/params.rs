//! Rescue-Prime parameters for BabyBear.
//!
//! - State width: 4 (rate=2, capacity=2)
//! - Rounds: 7
//! - S-box exponent α = 7, inverse α^{-1} = 1725656503 (mod p-1)
//! - MDS: 4×4 Cauchy matrix over BabyBear
//! - Round constants: derived from SHAKE-256("phantom-stark-rescue-prime-v1")

use crate::field::BabyBear;

/// State width (rate + capacity).
pub const STATE_WIDTH: usize = 4;

/// Rate: number of field elements absorbed/squeezed per permutation.
pub const RATE: usize = 2;

/// Capacity: number of field elements reserved for security.
pub const CAPACITY: usize = 2;

/// Number of rounds.
pub const NUM_ROUNDS: usize = 7;

/// S-box exponent: α = 7.
/// gcd(7, p-1) = 1, so x^7 is a permutation of F_p.
pub const ALPHA: u64 = 7;

/// Inverse S-box exponent: α^{-1} mod (p-1).
/// 7^{-1} mod 2013265920 = 1725656503
/// Used in the inverse permutation (for verifying Rescue-Prime).
pub const ALPHA_INV: u64 = 1725656503;

/// MDS matrix (4×4 Cauchy matrix).
/// MDS[i][j] = 1 / (i + j + STATE_WIDTH) mod p
/// where x_k = k for k in 0..4, y_k = k+4 for k in 0..4.
pub const MDS: [[u32; STATE_WIDTH]; STATE_WIDTH] = [
    [1509949441, 1610612737, 1677721601, 862828252],
    [1610612737, 1677721601, 862828252, 1761607681],
    [1677721601, 862828252, 1761607681, 447392427],
    [862828252, 1761607681, 447392427, 1811939329],
];

/// Round constants for the forward S-box (x^alpha) half-round.
/// Derived from SHAKE-256("phantom-stark-rescue-prime-v1").
/// Layout: ROUND_CONSTANTS_FWD[round][state_element]
///
/// SECURITY NOTE: These constants were generated by expanding a seed string through
/// SHAKE-256, but the generation script (`scripts/generate_constants.py`) is not
/// included in this repository. Without the ability to independently regenerate
/// these values, there is no way to verify they are "nothing-up-my-sleeve" numbers.
/// A backdoored set of round constants could create algebraic relations that allow
/// the hash to be inverted or collisions to be found efficiently. Users who require
/// verifiable parameters should regenerate using the documented seed and compare.
/// See: https://eprint.iacr.org/2020/1143 Section 5.2 for discussion of trapdoored
/// round constants in algebraic hash functions.
pub const ROUND_CONSTANTS_FWD: [[u32; STATE_WIDTH]; NUM_ROUNDS] = [
    [221313144, 2007526069, 1299701019, 1986755544],
    [1755894568, 102190626, 1240397735, 1907150191],
    [1421147589, 732289954, 1157292671, 324363395],
    [1265232470, 13823700, 745482314, 1148668129],
    [1008865738, 1644251911, 1749489442, 147311406],
    [1653274761, 193631221, 1725289294, 371633085],
    [1976034540, 119579711, 383411579, 1769795143],
];

/// Round constants for the backward S-box (x^{α^{-1}}) half-round.
/// Derived from SHAKE-256("phantom-stark-rescue-prime-v1").
/// Layout: ROUND_CONSTANTS_BWD[round][state_element]
pub const ROUND_CONSTANTS_BWD: [[u32; STATE_WIDTH]; NUM_ROUNDS] = [
    [1234520684, 1079509330, 217974626, 1091844924],
    [1446885454, 1003667530, 220556497, 387588351],
    [76929610, 696542140, 1452415750, 1741513170],
    [506582024, 844004530, 851187460, 138772132],
    [1496701819, 634270435, 1128594501, 1050655421],
    [789527632, 1793528336, 566861968, 365046495],
    [1227822331, 529190217, 1424333162, 887705538],
];

/// Get MDS matrix as field elements.
pub fn mds_matrix() -> [[BabyBear; STATE_WIDTH]; STATE_WIDTH] {
    let mut m = [[BabyBear::ZERO; STATE_WIDTH]; STATE_WIDTH];
    for i in 0..STATE_WIDTH {
        for j in 0..STATE_WIDTH {
            m[i][j] = BabyBear::new(MDS[i][j]);
        }
    }
    m
}

/// Get forward round constants as field elements.
pub fn round_constants_fwd() -> [[BabyBear; STATE_WIDTH]; NUM_ROUNDS] {
    let mut rc = [[BabyBear::ZERO; STATE_WIDTH]; NUM_ROUNDS];
    for r in 0..NUM_ROUNDS {
        for i in 0..STATE_WIDTH {
            rc[r][i] = BabyBear::new(ROUND_CONSTANTS_FWD[r][i]);
        }
    }
    rc
}

/// Get backward round constants as field elements.
pub fn round_constants_bwd() -> [[BabyBear; STATE_WIDTH]; NUM_ROUNDS] {
    let mut rc = [[BabyBear::ZERO; STATE_WIDTH]; NUM_ROUNDS];
    for r in 0..NUM_ROUNDS {
        for i in 0..STATE_WIDTH {
            rc[r][i] = BabyBear::new(ROUND_CONSTANTS_BWD[r][i]);
        }
    }
    rc
}
