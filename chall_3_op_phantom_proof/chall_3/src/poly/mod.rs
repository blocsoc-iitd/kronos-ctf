//! Polynomial types and operations over BabyBear.

pub mod fft;

use crate::field::BabyBear;

/// A polynomial represented by its coefficients in ascending order of degree.
/// coeffs[i] is the coefficient of x^i.
#[derive(Clone, Debug)]
pub struct DensePolynomial {
    pub coeffs: Vec<BabyBear>,
}

impl DensePolynomial {
    /// Create a new polynomial from coefficients (ascending degree order).
    pub fn new(coeffs: Vec<BabyBear>) -> Self {
        let mut p = Self { coeffs };
        p.trim();
        p
    }

    /// The zero polynomial.
    pub fn zero() -> Self {
        Self { coeffs: vec![] }
    }

    /// A constant polynomial.
    pub fn constant(c: BabyBear) -> Self {
        if c.is_zero() {
            Self::zero()
        } else {
            Self { coeffs: vec![c] }
        }
    }

    /// Create a polynomial from evaluations using inverse FFT.
    /// The evaluations must be over a domain of size 2^k generated by `generator`.
    pub fn from_evaluations(evals: &[BabyBear], generator: BabyBear) -> Self {
        let coeffs = fft::ifft(evals, generator);
        Self::new(coeffs)
    }

    /// Degree of the polynomial. Returns None for the zero polynomial.
    pub fn degree(&self) -> Option<usize> {
        if self.coeffs.is_empty() {
            None
        } else {
            Some(self.coeffs.len() - 1)
        }
    }

    /// Check if this is the zero polynomial.
    pub fn is_zero(&self) -> bool {
        self.coeffs.is_empty()
    }

    /// Number of coefficients (degree + 1, or 0 for the zero polynomial).
    pub fn len(&self) -> usize {
        self.coeffs.len()
    }

    /// Evaluate the polynomial at a point using Horner's method.
    pub fn evaluate(&self, x: BabyBear) -> BabyBear {
        if self.coeffs.is_empty() {
            return BabyBear::ZERO;
        }
        let mut result = *self.coeffs.last().unwrap();
        for i in (0..self.coeffs.len() - 1).rev() {
            result = result * x + self.coeffs[i];
        }
        result
    }

    /// Evaluate the polynomial at multiple points.
    pub fn evaluate_batch(&self, points: &[BabyBear]) -> Vec<BabyBear> {
        points.iter().map(|&x| self.evaluate(x)).collect()
    }

    /// Evaluate on a domain of size 2^k using FFT.
    /// `generator` is the generator of the domain.
    pub fn evaluate_fft(&self, domain_size: usize, generator: BabyBear) -> Vec<BabyBear> {
        let mut padded = self.coeffs.clone();
        padded.resize(domain_size, BabyBear::ZERO);
        fft::fft(&padded, generator)
    }

    /// Evaluate on a coset: {offset * g^i : i = 0..domain_size-1}.
    pub fn evaluate_on_coset(
        &self,
        domain_size: usize,
        generator: BabyBear,
        offset: BabyBear,
    ) -> Vec<BabyBear> {
        // Multiply coefficient i by offset^i, then FFT
        let mut shifted = vec![BabyBear::ZERO; domain_size];
        let mut offset_pow = BabyBear::ONE;
        for i in 0..self.coeffs.len().min(domain_size) {
            shifted[i] = self.coeffs[i] * offset_pow;
            offset_pow = offset_pow * offset;
        }
        fft::fft(&shifted, generator)
    }

    /// Polynomial addition.
    pub fn add(&self, other: &Self) -> Self {
        let len = self.coeffs.len().max(other.coeffs.len());
        let mut result = vec![BabyBear::ZERO; len];
        for i in 0..self.coeffs.len() {
            result[i] = result[i] + self.coeffs[i];
        }
        for i in 0..other.coeffs.len() {
            result[i] = result[i] + other.coeffs[i];
        }
        Self::new(result)
    }

    /// Polynomial subtraction.
    pub fn sub(&self, other: &Self) -> Self {
        let len = self.coeffs.len().max(other.coeffs.len());
        let mut result = vec![BabyBear::ZERO; len];
        for i in 0..self.coeffs.len() {
            result[i] = result[i] + self.coeffs[i];
        }
        for i in 0..other.coeffs.len() {
            result[i] = result[i] - other.coeffs[i];
        }
        Self::new(result)
    }

    /// Polynomial multiplication (schoolbook for small polys, or FFT-based).
    pub fn mul(&self, other: &Self) -> Self {
        if self.is_zero() || other.is_zero() {
            return Self::zero();
        }
        let result_len = self.coeffs.len() + other.coeffs.len() - 1;
        let mut result = vec![BabyBear::ZERO; result_len];
        for i in 0..self.coeffs.len() {
            for j in 0..other.coeffs.len() {
                result[i + j] = result[i + j] + self.coeffs[i] * other.coeffs[j];
            }
        }
        Self::new(result)
    }

    /// Scalar multiplication.
    pub fn scale(&self, scalar: BabyBear) -> Self {
        Self::new(self.coeffs.iter().map(|&c| c * scalar).collect())
    }

    /// Polynomial division with remainder: returns (quotient, remainder).
    /// self = quotient * divisor + remainder
    pub fn div_rem(&self, divisor: &Self) -> (Self, Self) {
        if divisor.is_zero() {
            panic!("Division by zero polynomial");
        }
        if self.is_zero() || self.coeffs.len() < divisor.coeffs.len() {
            return (Self::zero(), self.clone());
        }

        let mut remainder = self.coeffs.clone();
        let divisor_lead_inv = divisor.coeffs.last().unwrap().inverse().unwrap();
        let quotient_len = remainder.len() - divisor.coeffs.len() + 1;
        let mut quotient = vec![BabyBear::ZERO; quotient_len];

        for i in (0..quotient_len).rev() {
            let coeff = remainder[i + divisor.coeffs.len() - 1] * divisor_lead_inv;
            quotient[i] = coeff;
            for j in 0..divisor.coeffs.len() {
                remainder[i + j] = remainder[i + j] - coeff * divisor.coeffs[j];
            }
        }

        (Self::new(quotient), Self::new(remainder))
    }

    /// Remove trailing zero coefficients.
    fn trim(&mut self) {
        while self.coeffs.last().map_or(false, |c| c.is_zero()) {
            self.coeffs.pop();
        }
    }
}

/// Construct the vanishing polynomial Z_H(x) = x^n - 1 for a domain of size n.
pub fn vanishing_poly(n: usize) -> DensePolynomial {
    let mut coeffs = vec![BabyBear::ZERO; n + 1];
    coeffs[0] = -BabyBear::ONE;
    coeffs[n] = BabyBear::ONE;
    DensePolynomial::new(coeffs)
}

impl std::ops::Add for &DensePolynomial {
    type Output = DensePolynomial;
    fn add(self, rhs: Self) -> DensePolynomial {
        DensePolynomial::add(self, rhs)
    }
}

impl std::ops::Sub for &DensePolynomial {
    type Output = DensePolynomial;
    fn sub(self, rhs: Self) -> DensePolynomial {
        DensePolynomial::sub(self, rhs)
    }
}

impl std::ops::Mul for &DensePolynomial {
    type Output = DensePolynomial;
    fn mul(self, rhs: Self) -> DensePolynomial {
        DensePolynomial::mul(self, rhs)
    }
}
